    def get(self, key: str, language: str = "ru", **kwargs) -> str:
        texts = self.texts.get(language, self.texts["ru"])
        text = texts.get(key, self.texts["ru"].get(key, key))
        if kwargs:
            try:
                return text.format(**kwargs)
            except KeyError:
                return text
        return text
    def __call__(self, key: str, language: str = "ru", **kwargs) -> str:
        return self.get(key, language, **kwargs)
