def get_localized_field(obj, field_name: str, language: str = 'ru'):
    if language == 'en':
        en_field = f"{field_name}_en"
        if hasattr(obj, en_field):
            en_value = getattr(obj, en_field)
            if en_value:
                return en_value
    if hasattr(obj, field_name):
        return getattr(obj, field_name)
    return ""
